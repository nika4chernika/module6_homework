
function primeNumber(number) {
    if (number == 2) {
        return 'Число простое';
     }
    if (number <= 1 || number > 1000){
      return 'Данные неверны';
    } else {
        for (let i = 2; i < number; i++) {
        if (number % i === 0) {
          return 'Число составное';
        } else {
           return 'Число простое';
        }    
     }
     }
  }
  
console.log(primeNumber(80))
  
  
